@bot.message_handler(func=lambda message: True)
def handle_message(message):
    user_id = message.chat.id
    text = message.text

    if text == "–¢—Ä–µ–Ω–∏—Ä–æ–≤–∞—Ç—å":
        start_training(message, use_errors=False)
    elif text == "–û—à–∏–±–∫–∏":
        show_errors_menu(message)
    elif text == "–ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é":
        send_main_menu(message)
    elif text == "–¢—Ä–µ–Ω–∏—Ä–æ–≤–∞—Ç—å –æ—à–∏–±–∫–∏":
        start_training(message, use_errors=True)
    elif user_id in user_data and user_data[user_id]['training_mode'] is not None:
        check_answer(message)
    else:
        bot.send_message(user_id, "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∏—Å–ø–æ–ª—å–∑—É–π –∫–Ω–æ–ø–∫–∏ –¥–ª—è –Ω–∞–≤–∏–≥–∞—Ü–∏–∏.", reply_markup=main_menu_keyboard)


# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –Ω–∞—á–∞–ª–∞ —Ç—Ä–µ–Ω–∏—Ä–æ–≤–∫–∏
def start_training(message, use_errors=False):
    user_id = message.chat.id
    if use_errors and not user_data[user_id]['errors']:
        bot.send_message(user_id, "–£ —Ç–µ–±—è –ø–æ–∫–∞ –Ω–µ—Ç –æ—à–∏–±–æ–∫ –¥–ª—è —Ç—Ä–µ–Ω–∏—Ä–æ–≤–∫–∏.", reply_markup=main_menu_keyboard)
        return
    user_data[user_id]['training_mode'] = 'errors' if use_errors else 'all'
    send_question(message)

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –≤–æ–ø—Ä–æ—Å–∞
def send_question(message):
    user_id = message.chat.id
    if user_data[user_id]['training_mode'] == 'errors':
        current_words = {word: words[word] for word in user_data[user_id]['errors']}
    else:
        current_words = words

    if not current_words:
        bot.send_message(user_id, "–°–ø–∏—Å–æ–∫ —Å–ª–æ–≤ –ø—É—Å—Ç. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –¥–æ–±–∞–≤—å —Å–ª–æ–≤–∞ –≤ –º–∞—Å—Å–∏–≤ words.", reply_markup=main_menu_keyboard)
        user_data[user_id]['training_mode'] = None
        return

    word, options = random.choice(list(current_words.items()))
    correct_option = options[0]  # –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç –≤—Å–µ–≥–¥–∞ –ø–µ—Ä–≤—ã–π
    random.shuffle(options)  # –ü–µ—Ä–µ–º–µ—à–∏–≤–∞–µ–º –≤–∞—Ä–∏–∞–Ω—Ç—ã

    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç –∏ —Å–ª–æ–≤–æ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
    user_data[user_id]['current_word'] = word
    user_data[user_id]['correct_option'] = correct_option

    markup = telebot.types.ReplyKeyboardMarkup(one_time_keyboard=True, resize_keyboard=True)
    for option in options:
        markup.add(option)  # –ö–Ω–æ–ø–∫–∏ –æ—Å—Ç–∞—é—Ç—Å—è —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–º–∏
    markup.add("–ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é")  # –ö–Ω–æ–ø–∫–∞ –¥–ª—è –≤–æ–∑–≤—Ä–∞—Ç–∞ –≤ –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é
    bot.send_message(
        user_id,
        f"–í—ã–±–µ—Ä–∏ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ —É–¥–∞—Ä–µ–Ω–∏–µ: {word}",
        reply_markup=markup
    )

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –æ—Ç–≤–µ—Ç–∞
def check_answer(message):
    user_id = message.chat.id
    if message.text == "–ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é":
        send_main_menu(message)
        return

    correct_option = user_data[user_id]['correct_option']
    word = user_data[user_id]['current_word']
    training_mode = user_data[user_id]['training_mode']

    if message.text == correct_option:
        bot.send_message(user_id, f"‚úÖ –ü—Ä–∞–≤–∏–ª—å–Ω–æ! {correct_option}")
        # –£–¥–∞–ª—è–µ–º —Å–ª–æ–≤–æ –∏–∑ –æ—à–∏–±–æ–∫ —Ç–æ–ª—å–∫–æ –≤ —Ä–µ–∂–∏–º–µ —Ç—Ä–µ–Ω–∏—Ä–æ–≤–∫–∏ –æ—à–∏–±–æ–∫
        if training_mode == 'errors' and word in user_data[user_id]['errors']:
            user_data[user_id]['errors'].remove(word)
            if not user_data[user_id]['errors']:  # –ï—Å–ª–∏ –æ—à–∏–±–æ–∫ –±–æ–ª—å—à–µ –Ω–µ—Ç
                bot.send_message(user_id, "üéâ –í—Å–µ –æ—à–∏–±–∫–∏ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω—ã!", reply_markup=main_menu_keyboard)
                user_data[user_id]['training_mode'] = None
                return
    else:
        bot.send_message(user_id, f"‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ. –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç: {correct_option}")
        # –î–æ–±–∞–≤–ª—è–µ–º —Å–ª–æ–≤–æ –≤ —Å–ø–∏—Å–æ–∫ –æ—à–∏–±–æ–∫ —Ç–æ–ª—å–∫–æ –≤ —Ä–µ–∂–∏–º–µ –æ–±—ã—á–Ω–æ–π —Ç—Ä–µ–Ω–∏—Ä–æ–≤–∫–∏
        if training_mode == 'all' and word not in user_data[user_id]['errors']:
            user_data[user_id]['errors'].append(word)

    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–ª–µ–¥—É—é—â–∏–π –≤–æ–ø—Ä–æ—Å
    send_question(message)

